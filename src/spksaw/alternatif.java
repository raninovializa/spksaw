    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       DefaultTableModel tabMode;
       
       String kode_alternatif = jTextField1.getText();
       String nama_alternatif = jTextField2.getText();
       String nama_kriteria = jComboBox1.getSelectedItem().toString();
       String keterangan = jTextField3.getText();
       String crisp = jTextField4.getText();
       
       
       Object[] row = {"", kode_alternatif, nama_alternatif, nama_kriteria, keterangan, crisp};
       DefaultTableModel model = (DefaultTableModel) tabel.getModel();
       model.addRow(row);
       
                      
        if (kode_alternatif.equals("") | nama_alternatif.equals("") | nama_kriteria.equals("") | keterangan.equals("") | crisp.equals("")) {
            JOptionPane.showMessageDialog(null, "Pengisian Data Tidak Boleh Kosong");
        } else {
            try{
                koneksi classKoneksi = new koneksi();
                Statement st = con.createStatement();
                String sql = "INSERT INTO alternatif (kode_alternatif, nama_alternatif, nama_kriteria, keterangan, crisp) values ('"+kode_alternatif+"', '"+nama_alternatif+"', '"+nama_kriteria+"', '"+keterangan+"', '"+crisp+"')";
                int set = st.executeUpdate(sql);
                                                        
                bersih(); 
                                                
                this.setVisible(false);
                this.setVisible(true);
                                            
            } catch (Exception e) {    
             JOptionPane.showMessageDialog(null, e);
        }}  
    }//GEN-LAST:event_jButton1ActionPerformed


    public void tampil(){
        DefaultTableModel tabMode;
               
        koneksi classKoneksi = new koneksi();
        try{
            con = classKoneksi.getKoneksi();
            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM alternatif ORDER BY kode_alternatif ASC");
            int no = 1;
                        
            while(rs.next()) {
                String[] row = {Integer.toString(no),rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)};
                model.addRow(row);
                no++;
                               
                tabMode = new DefaultTableModel (null, row);
                tabel.setModel (tabMode);
            
                
            }
            tabel.setModel(model);            
        }catch(SQLException e){
            System.out.print(e.getMessage());
        }
    }
    
       
